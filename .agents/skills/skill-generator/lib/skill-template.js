/**
 * Generate a skill from a pattern
 * @param {Object} pattern - Source pattern
 * @param {Object} options - Generation options
 * @returns {Object} Generated skill
 */
export function generateSkillFromPattern(pattern, options = {}) {
    const skillId = generateSkillId(pattern);
    const skillName = generateSkillName(pattern);

    const skill = {
        id: skillId,
        name: skillName,
        title: toTitleCase(skillName.replace(/-/g, ' ')),
        description: pattern.message || `Skill for handling ${pattern.pattern}`,
        pattern: pattern.pattern,
        category: options.category || 'generated',
        triggers: extractTriggers(pattern),
        metrics: `${pattern.successCount}/${pattern.occurrences} success rate`,
        purpose: `Handle ${pattern.pattern} pattern automatically`,
        quickRef: generateQuickRef(skillName, pattern),
        implementation: generateImplementation(pattern),
        triggerSituation: pattern.context?.trigger || 'Pattern detected',
        timestamp: new Date().toISOString(),
        patternId: pattern.id
    };

    return skill;
}

/**
 * Generate skill ID from pattern
 */
function generateSkillId(pattern) {
    const base = pattern.pattern
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-|-$/g, '')
        .slice(0, 30);

    return `auto-${base}-${Date.now().toString(36)}`;
}

/**
 * Generate readable skill name
 */
function generateSkillName(pattern) {
    const name = pattern.pattern
        .toLowerCase()
        .replace(/[^a-z0-9\s]+/g, ' ')
        .trim()
        .replace(/\s+/g, '-')
        .slice(0, 40);

    return name || 'auto-skill';
}

/**
 * Extract triggers from pattern
 */
function extractTriggers(pattern) {
    const triggers = [];

    if (pattern.pattern) {
        triggers.push(pattern.pattern.slice(0, 30));
    }
    if (pattern.context?.trigger) {
        triggers.push(pattern.context.trigger);
    }
    if (pattern.message) {
        // Extract key words
        const words = pattern.message.match(/\b\w{4,}\b/g) || [];
        triggers.push(...words.slice(0, 2));
    }

    return [...new Set(triggers)].join(', ');
}

/**
 * Generate quick reference section
 */
function generateQuickRef(skillName, pattern) {
    return `### Apply Pattern

When you encounter: \`${pattern.pattern}\`

**Solution:**
\`\`\`
${pattern.message || 'Apply the pattern fix'}
\`\`\``;
}

/**
 * Generate implementation section
 */
function generateImplementation(pattern) {
    return `### Pattern Detection

Detect: \`${pattern.pattern}\`

### Fix Application

${pattern.message || 'Apply appropriate fix based on context'}

### Context

- Workflow: ${pattern.context?.workflow || 'any'}
- Step: ${pattern.context?.step || 'any'}
- Outcome: ${pattern.context?.outcome || 'success'}`;
}

/**
 * Convert to title case
 */
function toTitleCase(str) {
    return str.replace(/\w\S*/g, txt =>
        txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase()
    );
}

