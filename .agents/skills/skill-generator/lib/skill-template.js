/**
 * Create skill folder and files
 * @param {Object} skill - Skill data
 * @param {string} outputDir - Output directory
 * @returns {Object} Creation result
 */
export function createSkillFiles(skill, outputDir) {
    const skillPath = path.join(outputDir, skill.name);

    // Create directory
    fs.mkdirSync(skillPath, { recursive: true });

    // Generate SKILL.md content
    let content = SKILL_MD_TEMPLATE;
    for (const [key, value] of Object.entries(skill)) {
        content = content.replace(new RegExp(`{{${key}}}`, 'g'), value);
    }

    // Write SKILL.md
    fs.writeFileSync(path.join(skillPath, 'SKILL.md'), content);

    return {
        success: true,
        path: skillPath,
        files: ['SKILL.md']
    };
}

/**
 * Create registry entry for skill
 * @param {Object} skill - Skill data
 * @returns {Object} Registry entry
 */
export function createRegistryEntry(skill) {
    return {
        ...REGISTRY_ENTRY_TEMPLATE,
        id: skill.id,
        name: skill.name,
        path: `.agent/skills/${skill.name}`,
        generatedAt: skill.timestamp,
        generatedFrom: skill.patternId
    };
}

